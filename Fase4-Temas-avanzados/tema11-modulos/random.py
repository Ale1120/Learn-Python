import random

# numero aleatorio flotante >= 0 y < 1.0
print("Utilizando random.random() nos regresa numero random >= 0 y < 1.0 ",random.random())

# numero aleatorio flotante entre dos numeros >= n1 y < n2
print("Utilizando random.uniform() nos regresa numero random >= n1 y < n2 ",random.uniform(1,10))
